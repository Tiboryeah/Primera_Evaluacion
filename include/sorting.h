#ifndef SORTING_H
#define SORTING_H

/**
 * Ordena un arreglo utilizando el algoritmo de Burbuja (Bubble Sort).
 * Complejidad: O(n^2)
 */
void bubble_sort(int *arr, int n);

/**
 * Ordena un arreglo utilizando el algoritmo de Selección (Selection Sort).
 * Complejidad: O(n^2)
 */
void selection_sort(int *arr, int n);

/**
 * Ordena un arreglo utilizando el algoritmo de Inserción (Insertion Sort).
 * Complejidad: O(n^2)
 */
void insertion_sort(int *arr, int n);

#endif
